'''
Define a function max_of_three() that takes three numbers as arguments
and returns the largest of them.


'''

#Code

def max_of_three(num1,num2,num3):
    if num1 > num2:
        if num1>num3:
            return num1
        else:
            return num3
    elif num2>num3:
        return num2
    else:
        return num3
